function showPictures() {


let randomPicture  = Math.floor(Math.random()*5)

if(randomPicture === 0) {
   return "wrapper";
}
else if(randomPicture === 1) {
   return "wrapper1";
}
else if(randomPicture === 2){
   return "wrapper2";
}
else if(randomPicture === 3){
   return "wrapper3";
}
else if(randomPicture === 4){
   return "wrapper4";
}

}





